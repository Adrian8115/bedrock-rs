
#[derive(Debug, Copy, Clone, Eq, PartialEq)]
pub struct  Vec2{x: i32, z: i32}

#[derive(Debug, Copy, Clone)]
pub struct  Vec2f{x: f32, z: f32}

#[derive(Debug, Copy, Clone, Eq, PartialEq)]
pub struct  Vec3{x: i32, y: i32, z: i32}

#[derive(Debug, Copy, Clone)]
pub struct  Vec3f{x: f32, y: f32, z: f32}